//==================display a piture of 128*64 dots================
void display_graphic(char page,char column,char *dp)
{
	int i,j;
	for(i=0;i<8;i++)
	{
		address(page+i,column);
		for(j=0;j<128;j++)
		{
			transfer_data(*dp);    
			dp++; 		
		}
	}          
}

//==================display a piture of 128*64 dots================
void full_display(uchar data_left,uchar data_right)
{
	int i,j;
	for(i=0;i<8;i++)
	{
		LCD_CS_L;
		address(i+1,1);
		for(j=0;j<64;j++)
		{
			transfer_data(data_left);   
			transfer_data(data_right);    			 
		}
	}          
}

void display_32x32(uchar page,uchar column,uchar reverse,uchar *dp)
{
	uint i,j;
	for(i=0;i<4;i++)
	{
		address(page+i,column);
		for(j=0;j<31;j++)
		{
			if(reverse==1) 
				transfer_data(~*dp);
			else 
				transfer_data(*dp);
			dp++;
		}
	}
}


void display_16x16(uchar page,uchar column,uchar reverse,uchar *dp)
{
	uint i,j;
	for(i=0;i<2;i++)
	{
		address(page+i,column);
		for(j=0;j<16;j++)
		{
			if(reverse==1) 
				transfer_data(~*dp);
			else 
				transfer_data(*dp);
			dp++;
		}
	}
}


void display_8x16(char page,char column,char reverse,char *dp)
{
	int i,j;
	for(i=0;i<2;i++)
	{
		address(page+i,column);
		for(j=0;j<8;j++)
		{
			if(reverse==1)
				transfer_data(~*dp);
			else
				transfer_data(*dp);
			dp++;
		}
	}
}

void display_string_8x16(uint page,uint column,uchar *text)
{
	uint i=0,j,k,n;
	LCD_CS_L;
	while(text[i]>0x00)
	{	
		if((text[i]>=0x20)&&(text[i]<=0x7e))
		{
			j=text[i]-0x20;
			for(n=0;n<2;n++)
			{			
				address(page+n,column);
				for(k=0;k<8;k++)
				{
					transfer_data(ascii_table_8x16[j][k+8*n]);/*显示5x7的ASCII字到LCD上，y为页地址，x为列地址，最后为数据*/
				}
			}
			i++;
			column+=8;
			
		}
		else
		i++;
	}
}

void display_string_5x8(uint page,uint column,uchar *text)
{
	uint i=0,j,k;
	LCD_CS_L;
	while(text[i]>0x00)
	{	
		if((text[i]>=0x20)&&(text[i]<0x7e))
		{
			j=text[i]-0x20;
			address(page,column);
			for(k=0;k<5;k++)
			{
				transfer_data(ascii_table_5x8[j][k]);/*显示5x7的ASCII字到LCD上，y为页地址，x为列地址，最后为数据*/
			}
			transfer_data(0x00);
			i++;
			column+=6;
		}
		else
		i++;
	}
}




