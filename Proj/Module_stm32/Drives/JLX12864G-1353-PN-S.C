//===============clear all dot martrics=============
void clear_screen()
{
	unsigned char i,j;
	for(i=0;i<9;i++)
	{
		transfer_command(0xb0+i);
		transfer_command(0x10);
		transfer_command(0x00);
		for(j=0;j<132;j++)
		{
			transfer_data(0x00);
		}
	}
}


/*=======写指令========*/
void transfer_command(int data1)   
{
	char i;
	LCD_CS_L;
	LCD_RS_L;
// 	LCD_CS_L;
// 	rs=0;
	for(i=0;i<8;i++) 
	{
		LCD_SCK_L;
// 		sclk=0;
		if(data1&0x80)// sid=1;
			LCD_SDA_H;
		else// sid=0;
			LCD_SDA_L;
		LCD_SCK_H;
// 		sclk=1;
// 		delay_us(2);
		data1<<=1;	
	}
	LCD_CS_H;
// 	LCD_CS_H;
}

/*--------写数据------------*/
void transfer_data(int data1)
{
	char i;
	LCD_CS_L;
	LCD_RS_H;
// 	LCD_CS_L;
// 	rs=1;
	for(i=0;i<8;i++)
	{
		LCD_SCK_L;
// 		sclk=0;
		if(data1&0x80)// sid=1;
			LCD_SDA_H;
		else// sid=0;
			LCD_SDA_L;
		LCD_SCK_H;
// 		sclk=1;
// 		delay_us(2);
		data1<<=1;		
	}
	LCD_CS_H;
// 	LCD_CS_H;
}

void address(uchar page,uchar column)
{
	column=column-1;
	page=page-1;
	transfer_command(0xb0+page);
	transfer_command(((column>>4)&0x0f)+0x10);
	transfer_command(column&0x0f);
}

//=============delay time=====================
void delay(int i)               
{
	int j,k;
	for(j=0;j<i;j++)
	for(k=0;k<1100;k++);
}

